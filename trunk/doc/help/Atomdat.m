%% Atom.dat
% The atom.dat file contains information about different elements/isotopes,
% where each row defines an element/isotope. The meaning of the columns are
% the following:
%
% * *Name* of element, string. These labels can be used in the
% sw.unit_cell.label field to assign different atoms to crystallographic
% positions.
% * *Radius* in Angstrom, double. This gives the atomic radius for
% plotting.
% * *Color* RGB code, 3 integer. This gives the color for plottig the atom.
% * *Mass*, double. The mass of the element/isotope for unit cell mass
% calculation. 
% * *Long name*, string. Long name of the element/isotope.
%
% See also ion.dat.